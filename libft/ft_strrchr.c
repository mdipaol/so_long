#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*a;
	char	*b;

	i = 0;
	a = 0;
	b = (char *)s;
	while (b[i] != '\0')
	{
		if (b[i] == c)
			a = &b[i];
		i++;
	}
	if (c == '\0')
		return (&b[i]);
	return (a);
}
